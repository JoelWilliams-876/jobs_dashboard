# Function to fetch and parse U-3 and U-6 unemployment data
def fetch_unemployment_data():
    url = "https://www.bls.gov/news.release/empsit.t15.htm"
    response = requests.get(url)
    soup = BeautifulSoup(response.content, 'html.parser')
    table = soup.find("table", {"class": "regular"})

    if table:
        rows = table.find_all("tr")
        data = []

        # Extract U-3 and U-6 unemployment rates
        for row in rows:
            cols = row.find_all("td")
            if len(cols) >= 3:
                try:
                    period = cols[0].text.strip()
                    u3_rate = float(cols[1].text.strip())
                    u6_rate = float(cols[2].text.strip())
                    date = datetime.strptime(period, "%b %Y")
                    data.append({
                        "date": date,
                        "U-3": u3_rate,
                        "U-6": u6_rate
                    })
                except ValueError:
                    continue

        df = pd.DataFrame(data)
        return df
    else:
        print("Unemployment data table not found.")
        return pd.DataFrame()

# Function to fetch employment data
def fetch_bls_table_data():
    url = "https://data.bls.gov/dataViewer/view/timeseries/LNS11000000"
    response = requests.get(url)
    soup = BeautifulSoup(response.content, 'html.parser')
    table = soup.find("table", {"id": "seriesDataTable1"})

    if table:
        rows = table.find_all("tr")
        data = []

        for row in rows:
            cols = row.find_all("td")
            if len(cols) > 0:
                year = cols[0].text.strip()
                period = cols[1].text.strip()
                try:
                    value = float(cols[3].text.strip().replace(",", "").split("\r")[0])
                    month_code = period[1:]
                    concatenated_date = f"{month_code}-{year}"
                    data.append({
                        "date": concatenated_date,
                        "value": value
                    })
                except ValueError:
                    continue

        df = pd.DataFrame(data)
        df["date"] = pd.to_datetime(df["date"], format="%m-%Y")
        df["actual"] = df["value"]
        return df
    else:
        print("Table not found.")
        return pd.DataFrame()

